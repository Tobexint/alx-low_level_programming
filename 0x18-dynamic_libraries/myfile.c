#include <stdio.h>
#include <stdlib.h>

/**
 * add- calculates sum of two numbers
 * @a: number
 * @b: number
 *
 * Return: sum
 */
int add(int a, int b)
{
return (a + b);
}

/**
 * subtract- calculates the difference between two numbers
 * @a: number
 * @b: number
 *
 * Return: difference
 */
int subtract(int a, int b)
{
return (a - b);
}

/**
 * multiply- calculates the multiplication of two numbers
 * @: number
 * @b: number
 *
 * Return: multiplication
 */
int multiply(int a, int b)
{
return (a * b);
}

/**
 *divide- calculates the division of two numbers
 * @: number
 * @b: number
 *
 * Return: division
 */
int divide(int a, int b)
{
return (a / b);
}

/**
 * mod- calculates the remainder of two numbers
 * @: number
 * @b: number
 *
 * Return: modulus
 */

int mod(int a, int b)
{
return (a % b);
}
